public class Aresta {
    Integer v;
    String empresas;

    Aresta(Integer v, String empresas) {
        this.v = v;
        this.empresas = empresas;
    }

    public Integer getV() {
        return v;
    }

    public void setV(Integer v) {
        this.v = v;
    }

    public String getEmpresas() {
        return empresas;
    }

    public void setEmpresas(String empresas) {
        this.empresas = empresas;
    }
}
